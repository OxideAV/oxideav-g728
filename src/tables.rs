/// ITU-T G.728 Annex B `GQ` table: the 4 positive gain magnitudes.
///
/// `GQ(1) = 33/64 = 0.515625`; `GQ(i) = (7/4) * GQ(i-1)` for i = 2, 3, 4.
/// Exact rational values: 33/64, 231/256, 1617/1024, 11319/4096. The
/// spec's printed floating-point column agrees to the displayed
/// precision.
pub const GAIN_CB: [f32; GAIN_CB_SIZE] = [
    33.0 / 64.0,     // 0.515625
    231.0 / 256.0,   // 0.90234375
    1617.0 / 1024.0, // 1.579101562…
    11319.0 / 4096.0,// 2.763427734…
];

