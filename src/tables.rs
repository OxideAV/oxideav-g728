    fn shape_cb_last_row_matches_spec() {
        // Annex B, row index 127: 606, 2018, -1316, 4064, 398 / 2048.
        let expected = [
            606.0 / 2048.0,
            2018.0 / 2048.0,
            -1316.0 / 2048.0,
            4064.0 / 2048.0,
            398.0 / 2048.0,
        ];
        for k in 0..VECTOR_SIZE {
            assert!(
                (SHAPE_CB[127][k] - expected[k]).abs() < 1e-6,
                "row 127, col {k}: got {}, expected {}",
                SHAPE_CB[127][k],
                expected[k]
            );
        }
    }

    #[test]
    fn shape_cb_rows_are_distinct() {
        for i in 0..SHAPE_CB_SIZE {
            for j in (i + 1)..SHAPE_CB_SIZE {
                let mut diff = 0.0_f32;
                for k in 0..VECTOR_SIZE {
                    diff += (SHAPE_CB[i][k] - SHAPE_CB[j][k]).abs();
                }
                assert!(diff > 1e-6, "rows {i} and {j} are identical");
            }
        }
    }

    #[test]
    fn gain_cb_matches_spec_recurrence() {
        // GQ(1) = 33/64, GQ(i) = (7/4) * GQ(i-1) for i=2,3,4.
        assert!((GAIN_CB[0] - 0.515625).abs() < 1e-6);
        for i in 1..GAIN_CB_SIZE {
            let expect = (7.0 / 4.0) * GAIN_CB[i - 1];
            assert!(
                (GAIN_CB[i] - expect).abs() < 1e-5,
                "GQ({}): got {}, expected {}",
                i + 1,
                GAIN_CB[i],
                expect
            );
        }
    }

    #[test]
    fn gain_cb_is_monotone_increasing() {
        for k in 1..GAIN_CB_SIZE {
            assert!(GAIN_CB[k] > GAIN_CB[k - 1]);
        }
    }
