//! Codebook tables for the G.728 shape + gain VQ.
//!
//! Transcribed verbatim from **ITU-T Recommendation G.728 (09/92), Annex B**,
//! "Excitation shape and gain codebook tables":
//!
//! - [`SHAPE_CB`] — the 128-entry × 5-sample shape codebook (spec calls it
//!   `CODEBK` or `Y`). Each integer value in the spec is expressed in
//!   Q11 fixed point; the floating-point value is the integer divided by
//!   2048 (equivalently, a right-shift of the binary point by 11 bits).
//! - [`GAIN_CB`] — the 4 positive gain magnitudes from the `GQ` table.
//!   The bitstream carries a separate sign bit, which flips polarity to
//!   produce all 8 signed levels. The spec defines
//!   `GQ(1) = 33/64 = 0.515625` and `GQ(i) = (7/4) * GQ(i-1)` for
//!   `i = 2, 3, 4`, yielding exactly `0.515625, 0.90234375, 1.579101563,
//!   2.763427734`.
//!
//! The `GB` threshold table (used in the encoder's closed-form gain
//! decision in the reference implementation) is intentionally not
//! reproduced here — this crate's encoder does a brute-force search over
//! all four magnitudes, so `GB` is not needed.

